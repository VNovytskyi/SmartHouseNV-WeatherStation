void ESP_ReadAnswer()
{
	memset(RX_buff, 0, RX_buff_size);
	
	while(strlen(RX_buff) == 0)
	{
		HAL_UART_Receive(&huart2, (uint8_t *)RX_buff, RX_buff_size, 100);
	}
}

void PC_Send(char *str)
{
	HAL_UART_Transmit(&huart1,(uint8_t*)str,strlen(str),100);
}


