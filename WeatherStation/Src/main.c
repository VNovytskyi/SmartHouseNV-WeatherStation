#include "main.h"
#include "BME280.h"
#include "ESP8266.h"

char tempStr[128];

//For BME280
I2C_HandleTypeDef hi2c1;

//For PC
UART_HandleTypeDef huart1;

//For ESP-01
UART_HandleTypeDef huart2;

void SystemClock_Config(void);
